type _Data__BonusType = [number, string];

const convertBonusesData = (items: _Data__BonusType[]): GalaTreatsBonusType[] =>
  items.map<GalaTreatsBonusType>((item) => {
    const [day, officerName] = item;
    return {
      day,
      reward: {
        type: 'Officer',
        name: officerName,
        count: 1,
      },
    };
  });

